package miCodeSecnod;


public class Node 
{
	private int val;
    private Node next;
    private Node another;
    
	public int getVal() {
		return val;
	}
	public void setVal(int val) {
		this.val = val;
	}
	public Node getNext() {
		return next;
	}
	public void setNext(Node next) {
		this.next = next;
	}
	public Node getAnother() {
		return another;
	}
	public void setAnother(Node another) {
		this.another = another;
	}
}
